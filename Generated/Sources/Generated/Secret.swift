import Foundation

// swiftlint:disable trailing_whitespace trailing_comma
public enum Secret {
    private static let salt: [UInt8] = [
        0xa5, 0x28, 0xd6, 0x8c, 0x93, 0x94, 0x06, 0xce, 
        0xdb, 0xfd, 0xda, 0x99, 0x4d, 0xeb, 0xf3, 0x28, 
        0x24, 0xcd, 0x7d, 0xf6, 0xb4, 0x35, 0xd0, 0xed, 
        0x85, 0xa3, 0xd4, 0xc8, 0x70, 0x32, 0xcf, 0x02, 
        0x8a, 0x48, 0x2d, 0x04, 0xf7, 0x6a, 0x50, 0x8f, 
        0xb9, 0xa1, 0x8d, 0x7b, 0xea, 0x84, 0xe9, 0x99, 
        0xb4, 0x79, 0xee, 0xec, 0x30, 0xb2, 0xac, 0x4c, 
        0x31, 0xc7, 0x26, 0xb6, 0xbe, 0xe8, 0xf4, 0x3a, 
    ]

    public static var apiId: Int {
        let encoded: [UInt8] = [
            0x95, 
        ]

        return Int(decode(encoded, cipher: salt))!
    }

    public static var apiHash: String {
        let encoded: [UInt8] = [
            0xcc, 0x46, 0xa5, 0xe9, 0xe1, 0xe0, 0x59, 0xb7, 
            0xb4, 0x88, 0xa8, 
        ]

        return decode(encoded, cipher: salt)
    }

    public static func decode(_ encoded: [UInt8], cipher: [UInt8]) -> String {
        String(decoding: encoded.enumerated().map { (offset, element) in
            element ^ cipher[offset % cipher.count]
        }, as: UTF8.self)
    }
}
